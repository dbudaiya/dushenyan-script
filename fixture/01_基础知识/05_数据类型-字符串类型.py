# 1.字符串类型 str => "不以物喜不以己悲"
# 1.1单引号
a1 = '最喜爱歌手:李智恩'
print(a1)  # 最喜爱歌手:李智恩
print(type(a1))  # <type 'str'>

# 1.2双引号
a2 = "最喜爱歌手:李智恩"
print(a2)  # 最喜爱歌手:李智恩
print(type(a2))  # <type 'str'>

# 1.3三引号
a3 = '''最喜爱歌手:
李智恩'''
print(a3)
print(type(a3))  # <type 'str'>
