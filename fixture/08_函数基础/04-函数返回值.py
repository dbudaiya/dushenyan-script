# 计算奇数和偶数,并且返回
def a1(num):
    old = []
    even = []
    for i in num:
        if i % 2:
            old.append(i)
        else:
            even.append(i)
    return old, even


print(a1([10, 29, 34, 12, 16, 75, 99]))  # ([29, 75, 99], [10, 34, 12, 16]) 返回的是一个元组数据()

